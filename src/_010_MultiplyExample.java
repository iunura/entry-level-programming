public class _010_MultiplyExample {
    public static void main(String[] args) {
        int a, b;
        double x, y;

        a = 2;
        b = 3;
        x = 1.5;
        y = 0.5;

        System.out.println(a * b);
        System.out.println(a * y);
        System.out.println(x * y);

    }
}
