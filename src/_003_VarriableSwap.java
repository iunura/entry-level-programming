public class _003_VarriableSwap {

    public static void main(String[] args) {
        int a, b, c;

        a = 5;
        b = 10;

        c = a;
        a = b;
        b = c;

        System.out.println("a'nın değerini giriniz :" + a );
        System.out.println("b'nin değerini giriniz :" + b );

    }
}
